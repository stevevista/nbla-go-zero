constexpr int INVALID_POS = 361;


class NeighborVistor {
    int nbr_pars[4];
    int nbr_par_cnt = 0;
public:
    bool visited(int pos) {
        for (int i = 0; i < nbr_par_cnt; i++) {
            if (nbr_pars[i] == pos) {
                return true;
            }
        }
        nbr_pars[nbr_par_cnt++] = pos;
        return false;
    }
};

void GoBoard::reset() {
    
    for (int i = 0; i < 361; i++) {
        stones[i] = 0;
    }
}

void GoBoard::update_board(const int color, const int i) {

    if (i <0 || i >= 361 || stones[i]) {
        std::cout << i << std::endl;
        std::cout << stones[i] << std::endl;
        throw std::runtime_error("update board error");
    }

    stones[i] = color;
    stone_next[i] = i;
    group_ids[i] = i;

    int libs = 0;
    NeighborVistor vistor;

    for (int k = 0; k < 4; k++) {
        int ai = NEIGHBORS[k][i];
        if (ai == INVALID_POS) continue;
        
        if (stones[ai] == 0)
            libs++;
        else {
            int g = group_ids[ai];
            if (!vistor.visited(g)) {
                group_libs[g]--;
            }
        }
    }

    group_libs[i] = libs;

    for (int k = 0; k < 4; k++) {
        int ai = NEIGHBORS[k][i];
        if (ai == INVALID_POS) continue;

        if (stones[ai] == -color) {
            if (group_libs[group_ids[ai]] == 0) {
                remove_string(ai);
            }
        } else if (stones[ai] == color) {
            int ip = group_ids[i];
            int aip = group_ids[ai];

            if (ip != aip) {
                merge_strings(aip, ip);
            }
        }
    }

    // check whether we still live (i.e. detect suicide)
    if (group_libs[group_ids[i]] == 0) {
        remove_string(group_ids[i]);
    }
}

void GoBoard::remove_string(int i) {

    int pos = i;

    do {
        stones[pos]  = 0;

        NeighborVistor vistor;
        
        for (int k = 0; k < 4; k++) {
            int ai = NEIGHBORS[k][pos];
            if (ai == INVALID_POS || !stones[ai]) continue;

            if (!vistor.visited(group_ids[ai])) {
                group_libs[group_ids[ai]]++;
            }
        }

        pos = stone_next[pos];
    } while (pos != i);
}

void GoBoard::merge_strings(const int ip, const int aip) {

    /* loop over stones, update parents */
    int newpos = aip;

    do {
        // check if this stone has a liberty
        for (int k = 0; k < 4; k++) {
            int ai = NEIGHBORS[k][newpos];
            if (ai == INVALID_POS) continue;

            // for each liberty, check if it is not shared
            if (stones[ai] == 0) {
                // find liberty neighbors
                bool found = false;
                for (int kk = 0; kk < 4; kk++) {
                    int aai = NEIGHBORS[kk][ai];
                    if (aai == INVALID_POS || !stones[aai]) continue;

                    // friendly string shouldn't be ip
                    // ip can also be an aip that has been marked
                    if (group_ids[aai] == ip) {
                        found = true;
                        break;
                    }
                }

                if (!found) {
                    group_libs[ip]++;
                }
            }
        }

        group_ids[newpos] = ip;
        newpos = stone_next[newpos];
    } while (newpos != aip);

    /* merge stings */
    int tmp = stone_next[aip];
    stone_next[aip] = stone_next[ip];
    stone_next[ip] = tmp;
}


bool GoBoard::validate_moves(const std::vector<int>& moves) {

    GoBoard b;
    int color = 1;

    for (auto idx : moves) {

        if (idx < 0 || idx >= 361) {
            // pass or resign
        } else {
            if (b.stones[idx] != 0) {
                return false;
            }
            b.update_board(color, idx);
        }
            
        color = -color;
    }

    return true;
}